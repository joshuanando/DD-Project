	ID_Transaksi VARCHAR(14) PRIMARY KEY,
	Tanggal DATE,
	NAMA_PEMILIK VARCHAR2(50) NOT NULL,
	ALAMAT_PEMILIK VARCHAR2(50) NOT NULL,
	NO_KTP VARCHAR2(18) NOT NULL,
	NPWP VARCHAR2(20),
	NO_POLISI VARCHAR2(20) NOT NULL,
	DESKRIPSI_KENDARAAN VARCHAR2(50),
	
);


